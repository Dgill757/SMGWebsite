// Get stats by industry slug
export const getStatsByIndustrySlug = (slug: string): IndustryStatsData => {
  switch (slug) {
    case 'home-services':
      return homeServicesStats;
    case 'real-estate':
      return realEstateStats;
    case 'healthcare':
      return healthcareStats;
    case 'legal':
      return legalStats;
    case 'automotive':
      return automotiveStats;
    case 'professional-services':
      return professionalServicesStats;
    case 'landscaping':
      return landscapingStats;
    case 'customer-service':
      return customerServiceStats;
    default:
      // Return a default if no match (shouldn't happen with proper routing)
      return homeServicesStats;
  }
};
